public class Base05 {
    public static void main(String[] args) {
        int a = (int)4.6;
        System.out.println( "a : " + a );
        byte b = 100;
        System.out.println( "b : " + b );
        int c = 100;
        byte d = (byte)c;
        System.out.println( "c : " + c);
        int e = d;
        char ch = 65;
        System.out.println( "ch : " + ch );
        System.out.println( "ch + 1 : " + (ch + 1));
        int f = 67;
        char ch1 = (char)f;
        System.out.println( "ch1 : " + ch1 );
    }
}
